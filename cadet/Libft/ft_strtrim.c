#include "libft.h"

char		*ft_strtrim(char const *s1, char const *set);
static int	in_set(char c, const char *set);

static int	in_set(char c, const char *set)
{
	while (*set)
	{
		if (*set == c)
			return (1);
		set++;
	}
	return (0);
}

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*trim_str;
	size_t	start;
	size_t	end;
	size_t	len;

	if (!s1 || !set)
		return (NULL);
	start = 0;
	while (s1[start] && in_set(s1[start], set))
		start++;
	end = ft_strlen(s1);
	while (end > start && in_set(s1[end - 1], set))
		end--;
	len = end - start;
	trim_str = (char *)malloc((len + 1) * sizeof(char));
	if (!trim_str)
		return (NULL);
	ft_memcpy(trim_str, s1 + start, len);
	trim_str[len] = '\0';
	return (trim_str);
}
